package day04_variables;

public class PrimitiveDataTypes {

    public static void main (String [] args) {


        byte age = 34;
        System.out.println("age"); // "age" is not a variable name, it is a value (string)
        System.out.println(age); // age is a variable here. Datatype is now byte
        System.out.println(34); // 34 is a value, by default it is an int datatype as it is a whole number






    }


}
